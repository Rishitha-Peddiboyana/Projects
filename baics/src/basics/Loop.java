package typecasting;

import java.util.Scanner;

public class Loop {

	public static void main(String[] args) {
	int i =1;
		
	for (i=1;i<=15;i++)
		{
		
		System.out.println(i+" rishitha");
			
		}
		
		while(i<=10)
		{
			System.out.println(i+" rishitha");
			i++;
		}
		
		do {
			System.out.println(i+" rishitha");
			i++;
		}while(i<=5);
			

	}

}
